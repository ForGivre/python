# Python中有以下几种运算：
"""
加法运算：a+b
减法运算：a-b
乘法运算：a*b
除法运算：a/b
取余运算：a%b(返回a除以b的余数)
乘方运算：a**b
取模运算：a//b(返回a对于b的整除，如12//3=4,13.2//2=6)
"""
# 例如：
print("加法运算："+'1+1=',1+1)
print("减法运算："+"2-1=",2-1)
print("乘法运算："+"2*2=",2*2)
print("除法运算："+"12/2=",12/2)# 除法运算会返回浮点型，而取模运算返回整型
print("取余运算："+"12%2",12%2)
print("乘方运算："+"2**32=",2**32)
print("取模运算："+"12//2=",12//2)# 取模运算返回向下取整值
